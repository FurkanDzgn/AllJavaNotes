package Day11_Loops.Loops;

public class PracticeWhileLoop {
	public static void main(String[] args) {
		
	
	int num=11;
	
	do {
		System.out.print(num+", ");
		--num;
	}while(num>0); //0>0
	
	int num2=11;
	
	System.out.println();
	
	while(num2>0) {
		System.out.print(num2+", ");
		--num2;
	}
	}
	
	}



