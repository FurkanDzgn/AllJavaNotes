package Day10_IfStatements.Homework;

import java.util.Scanner;

public class BlaBla {
	public static void main(String[] args) {
		Scanner scan=new Scanner(System.in);
		int x=scan.nextInt();
		
		switch(x) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("2");
			break;
		default:
			System.out.println("Not");
		
		}
	}

}
